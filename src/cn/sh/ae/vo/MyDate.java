package cn.sh.ae.vo;

import java.io.Serializable;

/**
 * 
 */
public class MyDate implements Serializable {

	private static final long serialVersionUID = -1214023997294859490L;
	private int year;
	private int month;
	private int day;
	private int week;
	private int hour;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getWeek() {
		return week;
	}

	public void setWeek(int week) {
		this.week = week;
	}

	public int getHour() {
		return hour;
	}

	public void setHour(int hour) {
		this.hour = hour;
	}
}
